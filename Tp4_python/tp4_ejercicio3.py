#c) Imprime el último elemento de la tupla usando un índice negativo
frutas = ("manzana" , "banana","cereza" ,"naranja", "kiwi")
print(frutas[1]) #primera posicion en la tupla
print(frutas[:3]) # para imprimir hasta la posicion 2 [:3] son por cantidad de elementos
print(frutas[-1])# ultima posicion en la tupla